echo -e "${GREEN}  | |                      ____ _   _ ____     _____ ____                      | |  ${RESET}"
echo -e "${GREEN}  | |                     / ___| | | | __ )   |___ /|  _ \\                     | |  ${RESET}"
echo -e "${GREEN}  | |                    | |   | | | |  _ \\     |_ \\| | | |                    | |  ${RESET}"
echo -e "${GREEN}  | |                    | |___| |_| | |_) |   ___) | |_| |                    | |  ${RESET}"
echo -e "${GREEN}  | |                     \\____|\\___/|____/   |____/|____/                     | |  ${RESET}"
echo -e "${GREEN}  | |                                                                          | |  ${RESET}"
echo -e "${GREEN}__| |__________________________________________________________________________| |__${RESET}"
echo -e "${GREEN}__   __________________________________________________________________________   __${RESET}"
echo -e "${GREEN}  | |                                                                          | |  ${RESET}"